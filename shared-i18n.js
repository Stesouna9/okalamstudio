// ── Apply page translations ──
function applyPageTranslations(l) {
  const t = PAGE_T[l] || PAGE_T.en;

  // data-i18n attributes (innerHTML for those with HTML tags)
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) {
      // Use innerHTML for entries that might contain <em>, <strong>, <span>
      el.innerHTML = t[key];
    }
  });

  // id-based elements (collaborations + contact pages)
  const idKeys = [
    'c_sub','c_what_lead','c_what_p1','c_what_p2','c_what_p3',
    'c_what_label',
    'c_v1_t','c_v1_d','c_v2_t','c_v2_d','c_v3_t','c_v3_d',
    'c_serv_title',
    'c_s1_t','c_s1_d','c_s2_t','c_s2_d','c_s3_t','c_s3_d','c_s4_t','c_s4_d',
    'c_cr_label','c_cr_title','c_cr_note',
    'c_p1_niche','c_p1_title','c_p1_desc','c_p1_btn',
    'c_p2_niche','c_p2_title','c_p2_desc','c_p2_btn',
    'c_b_title','c_b_sub',
    'c_bc1_d','c_bc2_t','c_bc2_d','c_bc3_t','c_bc3_d',
  ];
  idKeys.forEach(key => {
    const el = document.getElementById(key);
    if (el && t[key] !== undefined) el.innerHTML = t[key];
  });

  // Nav links
  const navMap = {
    'studio.html':'nav_studio','projets.html':'nav_projects',
    'collaborations.html':'nav_collabs','contact.html':'nav_contact'
  };
  document.querySelectorAll('.ok-nav-link, .ok-nav-cta').forEach(a => {
    const href = a.getAttribute('href');
    if (href && navMap[href] && t[navMap[href]]) a.textContent = t[navMap[href]];
  });
}
